<p>
Nous ne pouvons passer sous silence les œuvres magistrales de Martel :
Les Abîmes (1894) où sont cités le Ragas de Dardennes et les pertes de l’Argens, puis, surtout
La France ignorée (1928) qui nous permet de rêver sur le Verdon, le Plan de Canjuers et l’arrière-pays
toulonnais.</p>
<p>
Cependant, la première liste et description des cavités du Var digne du nom fut réalisée par Louis Henseling,
conservateur de la bibliothèque municipale de Toulon. Dans la revue Zigzags à travers le Var, 8e série, 1938, il
publie dix pages concernant la description et la localisation de 52 gouffres et 56 grottes, sous le titre : « Abîmes,
avens, garagaïs, gouffres, ragages, trous, baumes et grottes ». Mais Henseling n’était pas un spéléologue,
c’était un randonneur, très curieux de tout ce qui touchait notre département.
</p>
<p>
Quand je suis entré au clan Eole, en 1953, la spéléologie était pratiquée par un grand nombre de groupes
affiliés aux Eclaireurs de France (EDF !). Ils n’avaient pas le nom de clubs, mais de clans. A Toulon, il y avait le
clan Eole et le clan des Scialets, à Paris, le clan Claude Sommer, à Lyon le clan des Tritons et, surtout, celui de
la Verna qui participait alors aux expéditions de la Pierre-Saint-Martin. A Toulon, le clan Eole était dirigé par Jean
Colombier, sous-officier de la Marine nationale. Je ne me souviens plus de quelle manière il s’était mis en
rapport avec le BRGM (Bureau de recherches géologiques et minières), lequel avait lancé la constitution d’un
fichier national des cavités. Aussi, au clan Eole, chaque nouvelle cavité explorée était-elle topographiée et
faisait l’objet de la rédaction d’une fiche BRGM qui nous était payée. Aujourd’hui, le BRGM a abandonné depuis
très longtemps l’entretien de ce fichier. Dans les années 1960, le clan Eol et le clan des Scialets avaient disparu
et Jean Colombier s’était inscrit à la Société des sciences naturelles de Toulon et du Var où il continuait à
pointer systématiquement toutes les cavités connues pour remplir des fiches identiques à celles du BRGM. Vers
1970, Jean Colombier se tuait en tombant d’un toit. Alain Lebas put récupérer toutes ses fiches, au nombre de
600 environ et, vers 1980, il me demanda de constituer un répertoire des cavités du Var du même modèle que
ce que faisait Yves Créac’h dans les Alpes-Maritimes. Mais, cela demandait beaucoup de travail et tombait à un
moment où je quittais l’administration pour m’établir à mon propre compte. Ne trouvant aucun collaborateur, je
laissai tomber. Il faut dire aussi, qu’à cette époque, nous n’avions pas encore les facilités apportées par
l’informatique et le traitement de texte. Taper, dessiner, mettre en pages et imprimer était beaucoup plus long
et compliqué. Quant aux modifications à apporter à un texte qui n’était pas gardé en mémoire, n’en parlons
pas.
</p>
<p>
Ce fichier a disparu de la filière spéléologique, sans doute se trouve-t-il encore au musée de Toulon. Mais,
des photocopies de fiches ont permis qu’il ne soit pas complètement perdu. Vers 1976, Richard Zinck et Michel
Lopez, aidés de Gérard Dou, entreprennent un fichier du plateau de Siou-Blanc, à partir de leurs documents
personnels et de ceux qu’ils peuvent récupérer auprès d’autres spéléologues.
</p>

<p>
A partir de 1984, après le congrès FFS de Toulon, Alain Franco et la nouvelle commission fichier du CDS Var, 
s’attellent à l’extension du fichier de Siou-Blanc à tout le département du Var. Alain Paillier participe à la
rédaction des fiches dont Jean Thomas obtient le tirage gratuit par le SDIS (Service départemental de l’incendie
et des secours).
</p>
<p>De 1986 à 1988, 282 nouvelles fiches sont créées. En 1988 est aussi publiée
la deuxième édition du fichier de Siou-Blanc (380 fiches). À partir de 1989 apparaissent chaque année des
additifs généraux du fichier du Var, ainsi que le fichier de la Sainte-Baume (111 fiches). Grâce au SDIS (Service
départemental d’incendie et de sécurité), tous les spéléologues à jour de leur cotisation peuvent recevoir
gracieusement les additifs.
</p>
<p>
En 1994, AlainFranco et Alain Paillier avaient édité 1239 fiches et leurs additifs Ils
doivent être loués pour ce travail d’autant plus important que le fichier n’était pas encore informatisé et que
tout était rédigé et corrigé à la machine à écrire. A la fin de cette période, des changements ont lieu au sein du
CDS 83.
</p>

<p>
Jean-Pierre Lucot s’occupe maintenant du fichier qu’il décide immédiatement d’informatiser. Il travaille
d’abord seul, saisissant tout le texte des fiches. Puis, il reçoit l’aide de Philippe Jubault, Frédéric Hay et
Stéphane Riviani (deMarseille). Ils se partagent le travail pour scanner et mettre en forme toutes les
topographies. En 1997, l’informatisation est terminée, mais les fiches sont toujours publiées sur papier à partir
du fichier informatique.
</p>

<p>
Fin 2000 apparaît enfin le premier
Cd-rom des cavités du Var qui sera ensuite mis à jour une fois par an. En 1990, Jean-Pierre Lucot avait hérité de
988 fiches, en 1995 elles avaient fait de nombreux bébés pour atteindre le chiffre de 1543. On atteignait 1778
fiches en 2000. Au 1er juin 2005, nous en sommes à 1961. Ce travail important n’est pas aisé, car il faut
constamment relancer les clubs ou les individuels pour fournir les topographies et localisation des nouvelles
cavités. À la longue, c’est un travail usant
</p>
<p>
Texte de Paul COURBON - diffusé avec son aimable autorisation (https://www.chroniques-souterraines.fr)
</p>

