Il nous appartient (nous spéléologue), de le faire vivre et de le développer et de faire perdurer cette
connaissance.<br>
Ce partage durera tant que nous serons respectueux du travail des autres (découvertes, topographies, etc) et
de l'environnement qui nous entoure
</p>

EOF;
